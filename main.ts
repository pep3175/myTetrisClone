// The grid is composed of 10x20 squares
function drawGrid() {
    for (let row = 0; row <= ROW; row++) {
        for (let col = 0; col <= COL; col++) {
            drawSquare(col, row, grid[row][col])
        }
    }
}
// Check if lines are completed
function checkLine() {
    let line: boolean = true
    let nbLinesCompleted: number = 0
    let checkAgain: boolean = true
    while (checkAgain == true) {
        checkAgain = false
        for (let row = ROW; row >= 0; row--) {
            line = true
            for (let col = 0; col <= COL; col++) {
                if (grid[row][col] == VACANT) { line = false }
            }
            if (line == true) {
                nbLines++
                nbLinesCompleted++
                bgPicture.fillRect(130, 30, 20, 20, BOARD)
                bgPicture.print(nbLines.toString(), 130, 30, 15)
                for (let r = row; r >= 1; r--) {
                    for (let c = 0; c <= COL; c++) {
                        grid[r][c] = grid[r - 1][c]
                    }
                }
                grid[0] = [VACANT, VACANT, VACANT, VACANT, VACANT, VACANT, VACANT, VACANT, VACANT, VACANT]
                checkAgain = true
            }
        }
    }
    info.changeScoreBy(50*(Math.exp(nbLinesCompleted)-1))
    drawGrid()
}
// Level up
function levelUp() {
    if (level < Math.floor(nbLines / 10)) {
        level++
        bgPicture.fillRect(130, 20, 20, 20, BOARD)
        bgPicture.print(level.toString(), 130, 20, 15)
        speed = 1000 - (level * 100)
        info.changeScoreBy(200 * level)
    }
}
// Create a new piece
function newPiece() {
    nextPiece.undraw()
    currentPiece = nextPiece
    currentPiece.x = 3
    currentPiece.y = -2
    currentPiece.draw()

    nextPiece = new Piece()
    nextPiece.draw()
}

///// Piece object creation //////////////////////////////////////////////
class Piece {
    tetromino: number[][][]
    color: number
    rotation: number
    active: number[][]
    x: number
    y: number
    constructor() {
        let rand: number = Math.randomRange(0, 6)
        this.tetromino = PIECES[rand]
        this.color = COLOR[rand]
        this.rotation = 0
        this.active = this.tetromino[this.rotation]
        this.x = 14
        this.y = 10
    }

    draw() {
        for (let row = 0; row <= this.active.length - 1; row++) {
            for (let col = 0; col <= this.active[row].length - 1; col++) {
                if (this.active[row][col] == 1) {
                    drawSquare(this.x + col, this.y + row, this.color)
                }
            }
        }
    }

    undraw() {
        for (let row = 0; row <= this.active.length - 1; row++) {
            for (let col = 0; col <= this.active[row].length - 1; col++) {
                if (this.active[row][col] == 1) {
                    drawSquare(this.x + col, this.y + row, VACANT)
                }
            }
        }
    }

    moveLeft() {
        if (!currentPiece.collision(-1, 0)) {
            this.undraw()
            this.x--
            this.draw()
        }
    }

    moveRight() {
        if (!currentPiece.collision(1, 0)) {
            this.undraw()
            this.x++
            this.draw()
        }
    }

    moveDown() {
        if (!currentPiece.collision(0, 1)) {
            this.undraw()
            this.y++
            this.draw()
        }
    }

    rotCCW() {
        let nextRot: number
        let prevRot: number
        let kick: number = 0

        prevRot = this.rotation
        this.undraw()

        if (this.rotation != 0) {
            nextRot = this.rotation - 1
        } else {
            nextRot = 3
        }

        this.rotation = nextRot
        this.active = this.tetromino[this.rotation]

        if (this.collision(0, 0)) {
            if (this.x < (COL) / 2) {
                kick = 1
            } else {
                kick = -1
            }
        }

        if (this.collision(kick, 0)) {
            this.rotation = prevRot
            this.active = this.tetromino[this.rotation]
            this.draw()
        } else {
            this.x = this.x + kick
            this.draw()
        }
    }

    rotCW() {
        let nextRot: number
        let prevRot: number
        let kick: number = 0

        prevRot = this.rotation
        this.undraw()

        if (this.rotation != 3) {
            nextRot = this.rotation + 1
        } else {
            nextRot = 0
        }

        this.rotation = nextRot
        this.active = this.tetromino[this.rotation]

        if (this.collision(0, 0)) {
            if (this.x < (COL)/2) {
                kick = 1
            }else{
                kick = -1
            }
        }

        if (this.collision(kick, 0)) {
            this.rotation = prevRot
            this.active = this.tetromino[this.rotation]
            this.draw()
        } else {
            this.x = this.x + kick
            this.draw()
        }
    }

    collision(x: number, y: number) {
        for (let row = 0; row <= this.active.length - 1; row++) {
            for (let col = 0; col <= this.active[row].length - 1; col++) {

                if (this.active[row][col] == 0) { continue }

                if ((this.x + col + x < 0) || (this.x + col + x > COL) || (this.y + row + y > ROW)) { return true }

                if (this.y < 0) { continue }

                if (grid[this.y + row + y][this.x + col + x] != VACANT) { return true }
            }
        }
        return false
    }

    lock() {
        for (let row = 0; row <= this.active.length - 1; row++) {
            for (let col = 0; col <= this.active[row].length - 1; col++) {
                if (this.active[row][col] == 1) {
                    grid[this.y + row][this.x + col] = this.color
                }
            }
        }
        drawGrid()
    }
}

